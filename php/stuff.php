<h4 class="media-heading">Rock Paper Scissors Online</h4>
<p>rps-online, is an implementation of the famous <strong><a href="http://en.wikipedia.org/wiki/Rock-paper-scissors">Rock Paper Scissors game</a></strong>, AKA <strong>Ro-Sham-bo</strong>. When I was a younger, I always used to play this, with friends to pass time, before I was introduced to Video Games. 
			<p>This is my way of paying homage to the game and a throwback to the <i>innocent and simple times.</i> 
			<p>The game can be played vs a program that analyzes you're responses and tries to guess you're next choice (be as unpredicitable as you can to beat it, otherwise, good luck beating it). Or it can be played VS another human, who is far away from you. The game can be played on a mobile or tablet or a PC/mac. It runs seemlessly on any device that has a modern browser, with a decent internet connection. Best of all, you don't have to register to play or anything. Just enter a random name, you're age and you're gender and start playing. I m not tracking you and anything. But what I am doing is recording your picks whenever you play, anonymously. And that's about it. And later on I hope to produce some information from the data I collected, which I would share on this site.  
			<p>The unique part about my implementation of the game is that it pulls random images from Google Search to represent Rock, paper and scissor. Many times the images shown are amusing, sometimes you don't even understand why a particular image is shown. But rest assured the images shown are always a result of Google Search for "Rock", "Paper" and "Scissors" terms. However, the relevency ranks are not always respected :P, hence the occasional surprise of the strange images shown occasionally.
			<p><a target="_new" href="http://rps-online.herokuapp.com">Go Play!!</a></p>
